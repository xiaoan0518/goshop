<template>
<!-- star-24"  动态类名 -->
<div class="star star-24" :class="'star-'+size">
  <!-- <span class="star-item on"></span>sss -->
  <!-- c代表on off  half -->
  <span class="star-item" v-for="(c, index) in starClasses" :key="index" :class="c"></span>
  
</div>

   
</template>

<script>
export default {
    props: {
      // 评分
      score:Number,
      // 大小
      size:Number
    },
    computed: {
      // 5个星星类名组成的数据在计算属性
      starClasses(){
        const arr = []
        //  拿到评分
        const {score} = this
        // 向下取整
        const scoreInteger = Math.floor(score)
        // console.log(scoreInteger);
        
        // // 向arr中添加n个on
        for (let index = 0; index < scoreInteger; index++) {
          // index是012345哪个符合就给它添加一个on
          arr.push('on')
        }
        // 向arr中添加0/1个half
        if (score*10 - scoreInteger*10>=5) {
          // console.log(score*10);
          // console.log(scoreInteger*10);
          arr.push('half')
        }
        // // 向arr中添加n个off
         if(arr.length<5) {
          arr.push('off')
        }
        return   arr
        
      }
    },
    data() {
        return {

        };
    },
};
</script>

